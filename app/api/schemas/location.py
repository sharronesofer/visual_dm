from marshmallow import Schema, fields

class LocationSchema(Schema):
    id = fields.Int(dump_only=True)
    name = fields.Str(required=True)
    description = fields.Str()
    type = fields.Str()
    is_discovered = fields.Bool()
    is_accessible = fields.Bool()
    coordinates = fields.Dict()
    size = fields.Dict()
    level = fields.Int()
    difficulty = fields.Str()
    resources = fields.Dict()
    npcs = fields.Dict()
    quests = fields.Dict()
    points_of_interest = fields.Dict()
    region_id = fields.Int()
    features = fields.Dict()
    objects = fields.Dict()
    state = fields.Dict()
    encounters = fields.Dict()
    created_at = fields.DateTime(dump_only=True)
    updated_at = fields.DateTime(dump_only=True) 